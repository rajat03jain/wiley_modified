package com.rajat.beans;

public class Items {
	private int id;
	private String type;
	private int price;
	private int qty;
	private String name;
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public int getId() {
		return id;
	}
	public void setId(int id) {
		this.id = id;
	}
	public String getType() {
		return type;
	}
	public void setType(String type) {
		this.type = type;
	}
	public int getPrice() {
		return price;
	}
	public void setPrice(int price) {
		this.price = price;
	}
	public int getQty() {
		return qty;
	}
	public void setQty(int qty) {
		this.qty = qty;
	}
	@Override
	public String toString() {
		return "Items [id=" + id + ", type=" + type + ", price=" + price + ", qty=" + qty + ", name=" + name + "]";
	}
	
	
	

}
